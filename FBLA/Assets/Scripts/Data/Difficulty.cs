using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/*
 * Difficulty class stores constant values that represent
 * easy, medium and hard; this makes changing these values
 * easy, as all classes refer to these values.
 */
public static class Difficulty {
	public static readonly int easy = 0;
	public static readonly int medium = 1;
	public static readonly int hard = 2;
} 